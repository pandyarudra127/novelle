export const updateProfileValidation = [
  body('name')
    .optional()
    .trim()
    .isLength({ min: 2, max: 100 }).withMessage('Name must be 2-100 characters'),
  body('bio')
    .optional()
    .trim()
    .isLength({ max: 500 }).withMessage('Bio cannot exceed 500 characters'),
  body('favoriteGenres')
    .optional()
    .isArray().withMessage('Favorite genres must be an array'),
  body('favoriteAuthors')
    .optional()
    .isArray().withMessage('Favorite authors must be an array'),
  body('readingGoal')
    .optional()
    .isInt({ min: 1 }).withMessage('Reading goal must be at least 1'),
]

